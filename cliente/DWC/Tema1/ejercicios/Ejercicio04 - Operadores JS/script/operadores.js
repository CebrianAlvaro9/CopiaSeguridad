
function miFuncion() {
    var n1 = prompt("numero 1");
    var n2 = prompt("numero 2");

    if(n1 == n2){
        alert( n1 + " y " + n2 + " son iguales ");
    }else if(n1 > n2) {
        alert( n1 + " es mayor que " + n2);
    }else if(n1 < n2) {
        alert( n1 + " es menor que " + n2);
    }

}